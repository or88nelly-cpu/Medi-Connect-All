import 'package:medi_connect/shared/auth/domain/entities/patient_entity.dart';

class PatientModel extends PatientEntity {
  const PatientModel({
    required super.id,
    super.userId,
    super.hospitalId,
    super.patientNo,
    super.registrationDate,
    super.title,
    super.maritalStatus,
    super.occupation,
    super.nationality,
    super.height,
    super.weight,
    super.guardianName,
    super.guardianRelationship,
    super.emergencyContactName,
    super.emergencyContactNumber,
    super.referredBy,
    super.registrationSource,
    super.remarks,
    super.isActive,
    super.createdAt,
    super.updatedAt,
    super.address,
    super.age,
    super.allergies,
    super.bloodGroup,
    super.chronicDiseases,
    super.dateOfBirth,
    super.emergencyContact,
    super.gender,
    super.insuranceNumber,
    super.insuranceProvider,
    super.patientId,
  });

  factory PatientModel.fromJson(Map<String, dynamic> json) {
    return PatientModel(
      id: json['id'] as String,
      userId: json['user_id'] as String?,
      hospitalId: json['hospital_id'] as String?,
      patientNo: json['patient_no'] as String?,
      registrationDate: json['registration_date'] != null
          ? DateTime.parse(json['registration_date'] as String)
          : null,
      title: json['title'] as String?,
      maritalStatus: json['marital_status'] as String?,
      occupation: json['occupation'] as String?,
      nationality: json['nationality'] as String?,
      height: json['height'] != null ? (json['height'] as num).toDouble() : null,
      weight: json['weight'] != null ? (json['weight'] as num).toDouble() : null,
      guardianName: json['guardian_name'] as String?,
      guardianRelationship: json['guardian_relationship'] as String?,
      emergencyContactName: json['emergency_contact_name'] as String?,
      emergencyContactNumber: json['emergency_contact_number'] as String?,
      referredBy: json['referred_by'] as String?,
      registrationSource: json['registration_source'] as String?,
      remarks: json['remarks'] as String?,
      isActive: json['is_active'] as bool? ?? true,
      createdAt: json['created_at'] != null
          ? DateTime.parse(json['created_at'] as String)
          : null,
      updatedAt: json['updated_at'] != null
          ? DateTime.parse(json['updated_at'] as String)
          : null,
      address: json['address'] as String?,
      age: json['age'] as int?,
      allergies: json['allergies'] as String?,
      bloodGroup: json['blood_group'] as String?,
      chronicDiseases: json['chronic_diseases'] as String?,
      dateOfBirth: json['date_of_birth'] != null
          ? DateTime.parse(json['date_of_birth'] as String)
          : null,
      emergencyContact: json['emergency_contact'] as String?,
      gender: json['gender'] as String?,
      insuranceNumber: json['insurance_number'] as String?,
      insuranceProvider: json['insurance_provider'] as String?,
      patientId: json['patient_id'] as String?,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'user_id': userId,
      'hospital_id': hospitalId,
      'patient_no': patientNo,
      'registration_date': registrationDate?.toIso8601String(),
      'title': title,
      'marital_status': maritalStatus,
      'occupation': occupation,
      'nationality': nationality,
      'height': height,
      'weight': weight,
      'guardian_name': guardianName,
      'guardian_relationship': guardianRelationship,
      'emergency_contact_name': emergencyContactName,
      'emergency_contact_number': emergencyContactNumber,
      'referred_by': referredBy,
      'registration_source': registrationSource,
      'remarks': remarks,
      'is_active': isActive,
      'created_at': createdAt?.toIso8601String(),
      'updated_at': updatedAt?.toIso8601String(),
      'address': address,
      'age': age,
      'allergies': allergies,
      'blood_group': bloodGroup,
      'chronic_diseases': chronicDiseases,
      'date_of_birth': dateOfBirth?.toIso8601String(),
      'emergency_contact': emergencyContact,
      'gender': gender,
      'insurance_number': insuranceNumber,
      'insurance_provider': insuranceProvider,
      'patient_id': patientId,
    };
  }

  factory PatientModel.fromEntity(PatientEntity entity) {
    return PatientModel(
      id: entity.id,
      userId: entity.userId,
      hospitalId: entity.hospitalId,
      patientNo: entity.patientNo,
      registrationDate: entity.registrationDate,
      title: entity.title,
      maritalStatus: entity.maritalStatus,
      occupation: entity.occupation,
      nationality: entity.nationality,
      height: entity.height,
      weight: entity.weight,
      guardianName: entity.guardianName,
      guardianRelationship: entity.guardianRelationship,
      emergencyContactName: entity.emergencyContactName,
      emergencyContactNumber: entity.emergencyContactNumber,
      referredBy: entity.referredBy,
      registrationSource: entity.registrationSource,
      remarks: entity.remarks,
      isActive: entity.isActive,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
      address: entity.address,
      age: entity.age,
      allergies: entity.allergies,
      bloodGroup: entity.bloodGroup,
      chronicDiseases: entity.chronicDiseases,
      dateOfBirth: entity.dateOfBirth,
      emergencyContact: entity.emergencyContact,
      gender: entity.gender,
      insuranceNumber: entity.insuranceNumber,
      insuranceProvider: entity.insuranceProvider,
      patientId: entity.patientId,
    );
  }
}
