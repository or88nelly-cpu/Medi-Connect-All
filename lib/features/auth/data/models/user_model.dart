/// Data model for User extending UserEntity.
/// Handles JSON deserialization and serialization for network payloads.
import '../../domain/entities/user_entity.dart';

class UserModel extends UserEntity {
  const UserModel({
    required super.id,
    required super.email,
    super.name,
    super.phoneNumber,
    required super.role,
    super.profileCompletionStatus,
    super.status,
    super.department,
    super.qualification,
    super.metadata,
    super.firstName,
    super.lastName,
    super.dateOfBirth,
    super.age,
    super.gender,
    super.profileImage,
    super.address,
    super.emergencyContact,
    super.bloodGroup,
    super.maritalStatus,
    super.employeeId,
    super.patientId,
    super.medicalRegistrationNumber,
    super.experience,
    super.specialization,
    super.consultationFee,
    super.availabilityStatus,
    super.staffRole,
    super.joiningDate,
    super.allergies,
    super.chronicDiseases,
    super.insuranceProvider,
    super.insuranceNumber,
    super.designation,
    super.accessLevel,
  });

  /// Factory constructor to parse user metadata and attributes from JSON.
  factory UserModel.fromJson(Map<String, dynamic> json) {
    // Check if fields are inside user_metadata or directly at the root
    final metadataMap = json['user_metadata'] as Map<String, dynamic>? ?? {};
    final parsedName = json['name'] as String? ?? metadataMap['name'] as String?;
    final parsedRole = json['role'] as String? ?? metadataMap['role'] as String? ?? 'patient';

    return UserModel(
      id: json['id'] as String,
      email: json['email'] as String? ?? '',
      name: parsedName,
      phoneNumber: json['phone'] as String? ?? json['phone_number'] as String?,
      role: parsedRole,
      profileCompletionStatus: json['profile_completion_status'] as bool? ?? false,
      status: json['status'] as String? ?? 'Pending Registration',
      department: json['department'] as String?,
      qualification: json['qualification'] as String?,
      metadata: json['metadata'] as Map<String, dynamic>?,
      firstName: json['first_name'] as String?,
      lastName: json['last_name'] as String?,
      dateOfBirth: json['date_of_birth'] as String?,
      age: json['age'] as int?,
      gender: json['gender'] as String?,
      profileImage: json['profile_image'] as String?,
      address: json['address'] as String?,
      emergencyContact: json['emergency_contact'] as String?,
      bloodGroup: json['blood_group'] as String?,
      maritalStatus: json['marital_status'] as String?,
      employeeId: json['employee_id'] as String?,
      patientId: json['patient_id'] as String?,
      medicalRegistrationNumber: json['medical_registration_number'] as String?,
      experience: json['experience'] as int?,
      specialization: json['specialization'] as String?,
      consultationFee: (json['consultation_fee'] as num?)?.toDouble(),
      availabilityStatus: json['availability_status'] as String? ?? 'Available',
      staffRole: json['staff_role'] as String?,
      joiningDate: json['joining_date'] as String?,
      allergies: json['allergies'] as String?,
      chronicDiseases: json['chronic_diseases'] as String?,
      insuranceProvider: json['insurance_provider'] as String?,
      insuranceNumber: json['insurance_number'] as String?,
      designation: json['designation'] as String?,
      accessLevel: json['access_level'] as String?,
    );
  }

  /// Maps the fields into standard JSON map.
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'email': email,
      'phone': phoneNumber,
      'name': name,
      'role': role,
      'profile_completion_status': profileCompletionStatus,
      'status': status,
      'department': department,
      'qualification': qualification,
      'metadata': metadata,
      'first_name': firstName,
      'last_name': lastName,
      'date_of_birth': dateOfBirth,
      'age': age,
      'gender': gender,
      'profile_image': profileImage,
      'address': address,
      'emergency_contact': emergencyContact,
      'blood_group': bloodGroup,
      'marital_status': maritalStatus,
      'employee_id': employeeId,
      'patient_id': patientId,
      'medical_registration_number': medicalRegistrationNumber,
      'experience': experience,
      'specialization': specialization,
      'consultation_fee': consultationFee,
      'availability_status': availabilityStatus,
      'staff_role': staffRole,
      'joining_date': joiningDate,
      'allergies': allergies,
      'chronic_diseases': chronicDiseases,
      'insurance_provider': insuranceProvider,
      'insurance_number': insuranceNumber,
      'designation': designation,
      'access_level': accessLevel,
    };
  }

  /// Conversion from entity back to model.
  factory UserModel.fromEntity(UserEntity entity) {
    return UserModel(
      id: entity.id,
      email: entity.email,
      name: entity.name,
      phoneNumber: entity.phoneNumber,
      role: entity.role,
      profileCompletionStatus: entity.profileCompletionStatus,
      status: entity.status,
      department: entity.department,
      qualification: entity.qualification,
      metadata: entity.metadata,
      firstName: entity.firstName,
      lastName: entity.lastName,
      dateOfBirth: entity.dateOfBirth,
      age: entity.age,
      gender: entity.gender,
      profileImage: entity.profileImage,
      address: entity.address,
      emergencyContact: entity.emergencyContact,
      bloodGroup: entity.bloodGroup,
      maritalStatus: entity.maritalStatus,
      employeeId: entity.employeeId,
      patientId: entity.patientId,
      medicalRegistrationNumber: entity.medicalRegistrationNumber,
      experience: entity.experience,
      specialization: entity.specialization,
      consultationFee: entity.consultationFee,
      availabilityStatus: entity.availabilityStatus,
      staffRole: entity.staffRole,
      joiningDate: entity.joiningDate,
      allergies: entity.allergies,
      chronicDiseases: entity.chronicDiseases,
      insuranceProvider: entity.insuranceProvider,
      insuranceNumber: entity.insuranceNumber,
      designation: entity.designation,
      accessLevel: entity.accessLevel,
    );
  }
}
